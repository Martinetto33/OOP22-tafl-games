package taflgames.common.code;

/**
 * This enumeration models the three possible results of a Match.
 */
public enum MatchResult {
    /**
     * The player has won.
     */
    VICTORY,
    /**
     * The player has lost.
     */
    DEFEAT,
    /**
     * Neither of the players has won or lost.
     */
    DRAW
}
